# Define a function called that takes two arguments, 'x' and 'y'.
def power_function(x, y):
    # Raise 'x' to the power of 'y' and assign the result to 'x'.
    x = x ** y
    # Return the value of 'x' to the caller.
    return print(x)


# Call the function with arguments 12 and 5.
power_function(12, 5)
